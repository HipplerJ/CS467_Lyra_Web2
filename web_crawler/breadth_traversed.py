from collections import defaultdict

class Queue:
    def __init__(self):
        self.items = []
        self.map = defaultdict(list)

    def enqueue(self, item):
        self.items.insert(0,item)

    def dequeue(self):
        return self.items.pop()

    def size(self):
        return len(self.items)
